package is.monkeydrivers.sensors;

public class travelCar {
    private int distance;
    private int time;

    public travelCar(int distance, int time) {
        this.distance = distance;
        this.time = time;
    }

    public int getDistance() {
        return distance;
    }

    public int getTime() {
        return time;
    }
}
